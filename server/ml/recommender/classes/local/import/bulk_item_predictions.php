namespace ml_recommender\local\import;


/**
 *
 * Upload recommendations per item from csv generated by the python recommender system.
 *
 * @package ml_recommender
 */
class bulk_item_predictions extends base_prediction_import  {

    /**
     * @inheritDoc
     */
    public function upload(): void {
        global $DB;

        // Open suggestions file for processing..
        $this->csvhandle = fopen($this->csvfilepath, 'r');
        if ($this->csvhandle === FALSE) {
            throw new \coding_exception('Could not open recommendations file for processing: ' . $this->csvfilepath);
        }

        // Skip headers.
        $headers = fgets($this->csvhandle);

        // Delete all existing recommendations.
        $DB->delete_records('ml_recommender_items');

        // Handling rows as strings will be faster than managing arrays via fgetcsv() for large files, because we have only a few fields.
        while (($row = fgets($this->csvhandle)) !== FALSE) {
            // Fields: target_iid, similar_iid, ranking.
            $fields = explode(',', $row);

            $score = round((float) $fields[2], 12);
            if ($score === -1.0) {
                continue;
            }

            // Separate the concatenated component type and id for target and recommended items.
            $target_unique_id = $fields[0];
            list($target_component, $target_item_id) = $this->get_component_id($target_unique_id);

            $similar_unique_id = $fields[1];
            list($similar_component, $similar_item_id) = $this->get_component_id($similar_unique_id);

            // Build record object.
            $record = new \stdClass();
            $record->unique_id = $similar_unique_id;
            $record->target_item_id = (int) $target_item_id;
            $record->target_component = $target_component;
            $record->target_area = null;
            $record->item_id = (int) $similar_item_id;
            $record->component = $similar_component;
            $record->area = null;
            $record->score = (float) $score;
            $record->time_created = time();

            // Write to database.
            $DB->insert_record('ml_recommender_items', $record);
        }

        // Wrap up.
        fclose($this->csvhandle);
    }
}
